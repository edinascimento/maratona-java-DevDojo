public class OperadoresDeComparacao {
    public static void main(String[] args) {

        boolean dezMaiorQueVinte = 10 > 20;
        System.out.println(dezMaiorQueVinte); // false

        boolean dezMenorQueVinte = 10 <= 20;
        System.out.println(dezMenorQueVinte);

        boolean cinoIgualACinco = 5 == 5;
        System.out.println(cinoIgualACinco);

        boolean cincoDiferenteDeCinco = 5 != 5;
        System.out.println(cincoDiferenteDeCinco);

    }
}
